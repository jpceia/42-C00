#include <unistd.h>

void	ft_print_comb2(void)
{
	char i;
	char j;
	char a[7];

	a[2] = ' ';
	a[5] = ',';
	a[6] = ' ';
	i = 0;
	while (i < 98)
	{
		a[0] = '0' + i / 10;
		a[1] = '0' + i % 10;
		j = i + 1;
		while (j < 100)
		{
			a[3] = '0' + j / 10;
			a[4] = '0' + j % 10;
			write(1, &a, 7);
			j++;
		}
		i++;
	}
	write(1, "98 99\n", 6);
}
